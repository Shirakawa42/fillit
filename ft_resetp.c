#include "fillit.h"

void	ft_resetp(char **tab, t_coord *c)
{
	int		x;
	int		y;

	y = 0;
	while (tab[y])
	{
		x = 0;
		while (tab[y][x])
		{
			if (tab[y][x] == c->c)
				tab[y][x] = '.';
			x++;
		}
		y++;
	}
}
